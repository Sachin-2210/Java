public class dattypes {
    public static void main(String[] args) {
        int num1 = 9;
        byte by = 127;
        short sh = 558;
        long l = 58648l;

        float f = 5.8f;
        double d = 5.8;

        char c = 'k';
        boolean b = true;

        System.out.println(num1 + by + sh + l + f + d + c + "b");
    }
}
